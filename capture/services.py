def service_name(port: int | None) -> str:
    if port is None:
        return ""
    return COMMON_PORTS.get(port, "")


def format_port(port: int | None) -> str:
    if port is None:
        return ""
    service = COMMON_PORTS.get(port)
    if service:
        return f"{port} ({service})"
    return str(port)
